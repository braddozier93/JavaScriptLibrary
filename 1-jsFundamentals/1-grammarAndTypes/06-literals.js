//literals are simply just values that WE as developers add to our code

//string literal
let car = "Ford";

//numeric literal
let december = 12;

//boolean literal--true or false
let tired = false;

//object literal {object in here--can have multiple keys and values in the object}
let soup = {
    a: "chicken noodle",
    b: "tomato",
    c: "beef and barley"
}

console.log(soup.a);

